package ex_03_Literals;

public class Lab029_Literals_P1 {
    public static void main(String[] args) {

        // Variable value is also called Literal.
        // Literals can be int, byte, float, double, boolean etc.

        int age = 65; // Integral Literal
        final float Pi = 3.14f; // float literal
        final int girl_age = 18; // girl_age is final/fixed can not be changed
    }

}
