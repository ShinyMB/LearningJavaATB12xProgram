package ex_01_Java_Basics;

public class Lab011_Common_Math {
    public static void main(String[] args) {
        System.out.println("2+2"); //Prints 2+2
        /*
        Strings are always in GREEN.
        Everything between "" will be printed
         */
        System.out.println(2 + 2);   //Prints 4
        System.out.println(2 * 2);   // Prints 4
        System.out.println(2 / 2);   //Prints 1
        System.out.println(2-2);     //Prints 0
        System.out.println(3/2);     //Prints 1
        /*
        println -> only prints integers, No decimal
         */
    }
}
