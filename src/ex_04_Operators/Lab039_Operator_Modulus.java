package ex_04_Operators;

public class Lab039_Operator_Modulus {

    public static void main(String[] args) {


        int a= 20;
        int b= 10;
        System.out.println(a%b);
        // Modulus always gives the reminder

        System.out.println(13%7);

        /*
        Modulus is used to find if a num is odd or even
        If reminder is 0, num is even
        If reminder is 1, num is odd
         */
    }
}
